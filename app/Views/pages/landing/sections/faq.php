    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const faqItems = document.querySelectorAll('.faq-item');
            let activeItem = null;

            // Initialize first FAQ as active
            faqItems[0]?.classList.add('active');
            const firstContent = faqItems[0]?.querySelector('.faq-content');
            if (firstContent) {
                firstContent.style.maxHeight = firstContent.scrollHeight + 'px';
            }

            faqItems.forEach(item => {
                const toggle = item.querySelector('.faq-toggle');
                const content = item.querySelector('.faq-content');

                toggle.addEventListener('click', () => {
                    const isActive = item.classList.contains('active');

                    // Close all FAQ items
                    faqItems.forEach(otherItem => {
                        otherItem.classList.remove('active');
                        const otherContent = otherItem.querySelector('.faq-content');
                        if (otherContent) {
                            otherContent.style.maxHeight = null;
                        }
                    });

                    // If clicked item wasn't active, open it
                    if (!isActive) {
                        item.classList.add('active');
                        content.style.maxHeight = content.scrollHeight + 'px';
                        activeItem = item;
                    } else {
                        activeItem = null;
                    }
                });
            });

            // Add keyboard navigation
            document.addEventListener('keydown', (e) => {
                if (e.key === 'ArrowDown' || e.key === 'ArrowUp') {
                    e.preventDefault();

                    const currentActiveIndex = activeItem ?
                        Array.from(faqItems).indexOf(activeItem) : -1;

                    let newIndex;
                    if (e.key === 'ArrowDown') {
                        newIndex = currentActiveIndex < faqItems.length - 1 ?
                            currentActiveIndex + 1 : 0;
                    } else {
                        newIndex = currentActiveIndex > 0 ?
                            currentActiveIndex - 1 : faqItems.length - 1;
                    }

                    faqItems[newIndex]?.querySelector('.faq-toggle')?.click();
                }
            });
        });
    </script>
